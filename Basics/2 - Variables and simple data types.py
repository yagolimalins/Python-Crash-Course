# Variável

message = "Hello Python World!"
print(message)

# String

name = "ada lovelace"
print(name.title())  # Titulo
print(name.upper())  # Maiusculo
print(name.lower())  # Minusculo

first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name  # Concatenação de strings
print(full_name)

print("Python")  # Caractere de escape
print("\tPython")  # Tabulação
print("Languages:\nPython\nC\nJavaScript")  # Nova linha
print("Languages:\n\tPython\n\tC\n\tJavaScript")  # Nova linha e tabulação

favorite_language = ' python '  # Remover espaços da direita, esquerda e extremidades
print(favorite_language.rstrip())
print(favorite_language.lstrip())
print(favorite_language.strip())

